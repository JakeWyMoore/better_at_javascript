// VARIABLES

// Create a Variable: var
var favoriteFood = 'pizza'
var numOfSlices = 8

console.log(favoriteFood)
console.log(numOfSlices)

// Create a Variable: let
let changeMe = 'Change from this'
console.log(changeMe)

changeMe = 'To this!!!'
console.log(changeMe)

// Create a Variable: const
const entree = 'tacos'
console.log(entree)

